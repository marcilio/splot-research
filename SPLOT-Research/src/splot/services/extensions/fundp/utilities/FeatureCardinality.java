package splot.services.extensions.fundp.utilities;

/** FeatureCardinality is used when the cardinality of a feature in a feature model is calculated. 
* 
* @author  PReCISE (research center of the University of FUNDP)
* @version 0.1
*/
public class FeatureCardinality {
	/** the minimum number of the cardinality */	
	public int minValue;
	
	/** the minimum number of the cardinality: for feature dummy */	
	public int minDummyValue;
	
	/** the maximum number of the cardinality */	
	public int maxValue;

	/** the maximum number of the cardinality: for feature dummy */	
	public int maxDummyValue;

}
